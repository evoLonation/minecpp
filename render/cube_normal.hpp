// 一个带有法向量信息的立方体
// location=0：vec3的顶点位置
// location=1：vec3的顶点法向量
class CubeNormal{
private:
   VertexBuffer vbo;
   VertexArray vao;
   VertexBuffer createVBO();
   VertexArray createVAO();
public:
   CubeNormal(): 
   vbo(createVBO()),
   vao(createVAO()){};
   ~CubeNormal() = default;

   VertexArray& getVertexArray(){return vao;}

   CubeNormal(const CubeNormal&) = delete;
   CubeNormal& operator=(const CubeNormal&) = delete;
};

